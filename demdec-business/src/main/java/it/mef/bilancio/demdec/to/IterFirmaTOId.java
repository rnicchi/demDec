package it.mef.bilancio.demdec.to;

import java.io.Serializable;

public class IterFirmaTOId implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8443658413062060440L;
	private Short fkSequIdIter;
	private Integer numeOrdine;


	public IterFirmaTOId() {}
	public IterFirmaTOId(Short fkSequIdIter, Integer numeOrdine) {
		this.fkSequIdIter = fkSequIdIter;
		this.numeOrdine = numeOrdine;
	}


	//getters & setters
	public Short getFkSequIdIter() {
		return fkSequIdIter;
	}
	public void setFkSequIdIter(Short fkSequIdIter) {
		this.fkSequIdIter = fkSequIdIter;
	}
	public Integer getNumeOrdine() {
		return numeOrdine;
	}
	public void setNumeOrdine(Integer numeOrdine) {
		this.numeOrdine = numeOrdine;
	}
	@Override
	public String toString() {
		return "IterFirmaTOId [fkSequIdIter=" + fkSequIdIter + ", numeOrdine="
				+ numeOrdine + "]";
	}
}
