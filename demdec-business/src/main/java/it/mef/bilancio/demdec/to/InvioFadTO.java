package it.mef.bilancio.demdec.to;


public class InvioFadTO extends AbstractDemTO implements java.io.Serializable{

	private static final long serialVersionUID = 1L;
	
	private String IdFascicoloAttoDecreto;
	private String IdDocumento;
	private String IdDocumentoRaccoltaProvvisoria;
	private String IdFascicoloRaccoltaProvvisoria;
	private String tipoDocumento;
	private String operazione;
	private String descrizione;
	private String statoFascicolo;
	
	private boolean daInviare;
	private boolean condivisibile;
	private boolean attivo;
	
	
	public String getStatoFascicolo() {
		return statoFascicolo;
	}
	public void setStatoFascicolo(String statoFascicolo) {
		this.statoFascicolo = statoFascicolo;
	}
	public String getDescrizione() {
		return descrizione;
	}
	public void setDescrizione(String descrizione) {
		this.descrizione = descrizione;
	}
	public String getIdFascicoloAttoDecreto() {
		return IdFascicoloAttoDecreto;
	}
	public void setIdFascicoloAttoDecreto(String idFascicoloAttoDecreto) {
		IdFascicoloAttoDecreto = idFascicoloAttoDecreto;
	}
	public String getIdDocumento() {
		return IdDocumento;
	}
	public void setIdDocumento(String idDocumento) {
		IdDocumento = idDocumento;
	}
	public String getIdDocumentoRaccoltaProvvisoria() {
		return IdDocumentoRaccoltaProvvisoria;
	}
	public void setIdDocumentoRaccoltaProvvisoria(
			String idDocumentoRaccoltaProvvisoria) {
		IdDocumentoRaccoltaProvvisoria = idDocumentoRaccoltaProvvisoria;
	}
	public String getIdFascicoloRaccoltaProvvisoria() {
		return IdFascicoloRaccoltaProvvisoria;
	}
	public void setIdFascicoloRaccoltaProvvisoria(
			String idFascicoloRaccoltaProvvisoria) {
		IdFascicoloRaccoltaProvvisoria = idFascicoloRaccoltaProvvisoria;
	}
	public String getTipoDocumento() {
		return tipoDocumento;
	}
	public void setTipoDocumento(String tipoDocumento) {
		this.tipoDocumento = tipoDocumento;
	}
	public String getOperazione() {
		return operazione;
	}
	public void setOperazione(String operazione) {
		this.operazione = operazione;
	}
	public boolean isDaInviare() {
		return daInviare;
	}
	public void setDaInviare(boolean daInviare) {
		this.daInviare = daInviare;
	}
	public boolean isCondivisibile() {
		return condivisibile;
	}
	public void setCondivisibile(boolean condivisibile) {
		this.condivisibile = condivisibile;
	}
	public boolean isAttivo() {
		return attivo;
	}
	public void setAttivo(boolean attivo) {
		this.attivo = attivo;
	}
	
	
	
}
