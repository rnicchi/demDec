package it.mef.bilancio.demdec.to;

import it.almavivaitalia.bilancio.commons.to.AbstractCommonTO;

public class FirmatarioTO extends AbstractCommonTO implements java.io.Serializable{

	private static final long serialVersionUID = 1L;

	private String numeroOrdineFirma;
	private String descFirmatario;
	private String tipoSegnatura;
	private String amministrazione;
	private String ufficio;
	private String numeroCdr;
	
	
	public String getNumeroOrdineFirma() {
		return numeroOrdineFirma;
	}
	public void setNumeroOrdineFirma(String numeroOrdineFirma) {
		this.numeroOrdineFirma = numeroOrdineFirma;
	}
	public String getDescFirmatario() {
		return descFirmatario;
	}
	public void setDescFirmatario(String descFirmatario) {
		this.descFirmatario = descFirmatario;
	}
	public String getTipoSegnatura() {
		return tipoSegnatura;
	}
	public void setTipoSegnatura(String tipoSegnatura) {
		this.tipoSegnatura = tipoSegnatura;
	}
	public String getAmministrazione() {
		return amministrazione;
	}
	public void setAmministrazione(String amministrazione) {
		this.amministrazione = amministrazione;
	}
	public String getUfficio() {
		return ufficio;
	}
	public void setUfficio(String ufficio) {
		this.ufficio = ufficio;
	}
	public String getNumeroCdr() {
		return numeroCdr;
	}
	public void setNumeroCdr(String numeroCdr) {
		this.numeroCdr = numeroCdr;
	}
	
	
	
	
	
}
