package it.almavivaitalia.bilancio.commons.to;

public class DescDocumentoTO extends AbstractCommonTO implements java.io.Serializable{

	private static final long serialVersionUID = 1L;
	
	private String id;
	private String textDescrizione;
	private String codiTipoDocumento;
    private Integer flagVisibilitaCdc;
	
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTextDescrizione() {
		return textDescrizione;
	}
	public void setTextDescrizione(String textDescrizione) {
		this.textDescrizione = textDescrizione;
	}
	public String getCodiTipoDocumento() {
		return codiTipoDocumento;
	}
	public void setCodiTipoDocumento(String codiTipoDocumento) {
		this.codiTipoDocumento = codiTipoDocumento;
	}
	
    public Integer getFlagVisibilitaCdc() {
		return flagVisibilitaCdc;
	}

	public void setFlagVisibilitaCdc(Integer flagVisibilitaCdc) {
		this.flagVisibilitaCdc = flagVisibilitaCdc;
	}

	
	
}
