package it.almavivaitalia.bilancio.commons.to;



public class EmailUtenteTO extends AbstractCommonTO implements java.io.Serializable{


	private String emailUtente;
	private Integer flagEmailPec;
	
	
	public EmailUtenteTO(){}
	
	public EmailUtenteTO(String emailUtente, Integer flagEmailPec) {
		this.emailUtente = emailUtente;
		this.flagEmailPec = flagEmailPec;
	}

	public String getEmailUtente() {
		return emailUtente;
	}

	public void setEmailUtente(String emailUtente) {
		this.emailUtente = emailUtente;
	}

	public Integer getFlagEmailPec() {
		return flagEmailPec;
	}

	public void setFlagEmailPec(Integer flagEmailPec) {
		this.flagEmailPec = flagEmailPec;
	}



	private static final long serialVersionUID = 1L;
}
