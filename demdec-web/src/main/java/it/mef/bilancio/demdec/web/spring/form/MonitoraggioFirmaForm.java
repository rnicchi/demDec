package it.mef.bilancio.demdec.web.spring.form;

import it.almavivaitalia.web.sh.form.AbstractForm;

public class MonitoraggioFirmaForm extends AbstractForm{
	
	private String tipoDecreto;
	private String numeDecreto;
	private String annoDecreto;
	
	private String allaFirma;
	private String firmato;
	private String statoFascicolo;
	
	public String getTipoDecreto() {
		return tipoDecreto;
	}
	
	public void setTipoDecreto(String tipoDecreto) {
		this.tipoDecreto = tipoDecreto;
	}
	
	public String getNumeDecreto() {
		return numeDecreto;
	}

	public void setNumeDecreto(String numeDecreto) {
		this.numeDecreto = numeDecreto;
	}

	public String getAnnoDecreto() {
		return annoDecreto;
	}
	
	public void setAnnoDecreto(String annoDecreto) {
		this.annoDecreto = annoDecreto;
	}

	public String getStatoFascicolo() {
		return statoFascicolo;
	}

	public String getAllaFirma() {
		return allaFirma;
	}

	public void setAllaFirma(String allaFirma) {
		this.allaFirma = allaFirma;
	}

	public String getFirmato() {
		return firmato;
	}

	public void setFirmato(String firmato) {
		this.firmato = firmato;
	}

	public void setStatoFascicolo(String statoFascicolo) {
		this.statoFascicolo = statoFascicolo;
	}
	
	public void clean(){
		
		this.setTipoDecreto("");
		this.setNumeDecreto("");
		this.setAnnoDecreto("");
		this.setAllaFirma("");
		this.setFirmato("");
		this.setStatoFascicolo("");
	}

}
