public class NsbfGestioneFascicoliServiceFault {

	public static final String UTENTE_NON_AUTORIZZATO_CODE = "1";
	public static final String UTENTE_NON_AUTORIZZATO_DESCRIPTION = "Utente non autorizzato.";
	
	public static final String FASCICOLO_PRESENTE_CODE = "2";
	public static final String FASCICOLO_PRESENTE_DESCRIPTION = "Fascicolo già presente";
	
	public static final String FASCICOLO_INESISTENTE_CODE = "3";
	public static final String FASCICOLO_INESISTENTE_DESCRIPTION = "Fascicolo inesistente";
	
	/**
	 * Fault Code
	 */
	private String faultCode;
	/**
	 * Fault String
	 */
	private String faultDescription;

	/**
	 * @return the faultCode
	 */
	public String getFaultCode() {
		return faultCode;
	}

	/**
	 * @param faultCode
	 *            the faultCode to set
	 */
	public void setFaultCode(String faultCode) {
		this.faultCode = faultCode;
	}

	/**
	 * @return the faultDescription
	 */
	public String getFaultDescription() {
		return faultDescription;
	}
	
	/**
	 * @param faultDescription
	 *            the faultDescription to set
	 */

	public void setFaultDescription(String faultDescription) {
		this.faultDescription = faultDescription;
	}
}
