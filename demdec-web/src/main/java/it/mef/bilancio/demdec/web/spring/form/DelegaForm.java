package it.mef.bilancio.demdec.web.spring.form;

import it.almavivaitalia.web.sh.form.AbstractForm;

public class DelegaForm extends AbstractForm{
	
	private String tipoFirma;
	private String delegante;
	private String delegato;
	
	private String tipoFirmaFind;
	private String delegatoFind;

	public String getTipoFirma() {
		return tipoFirma;
	}

	public void setTipoFirma(String tipoFirma) {
		this.tipoFirma = tipoFirma;
	}

	public String getDelegante() {
		return delegante;
	}

	public void setDelegante(String delegante) {
		this.delegante = delegante;
	}

	public String getDelegato() {
		return delegato;
	}

	public void setDelegato(String delegato) {
		this.delegato = delegato;
	}

	public String getTipoFirmaFind() {
		return tipoFirmaFind;
	}

	public void setTipoFirmaFind(String tipoFirmaFind) {
		this.tipoFirmaFind = tipoFirmaFind;
	}

	public String getDelegatoFind() {
		return delegatoFind;
	}

	public void setDelegatoFind(String delegatoFind) {
		this.delegatoFind = delegatoFind;
	}
	
	public void clear(){
		this.tipoFirma = null;
		this.delegante = null;
		this.delegato = null;
		this.tipoFirmaFind = null;
		this.delegatoFind = null;
	}

}
